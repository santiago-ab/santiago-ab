    public IModPI(int coordenada) {
        initComponents();
              
        Admin_PI ctrlPI = Admin_PI.getInstance();

        this.disponibilidad = ctrlPI.getPI(coordenada).get_Disponibilidad();
        this.coordenadaAnterior = coordenada;
        
        DefaultTableModel modelo = (DefaultTableModel) jTableRelacionadas.getModel();
        
        for(int i=0;i<12;i++){
            jComboBoxUbicacion.addItem(Data.ubicacion[i]);
            jComboBoxUbiObras.addItem(Data.ubicacion[i]);
        }
        
        jComboBoxUbicacion.setSelectedItem(ctrlPI.getPI(coordenada).get_Ubicacion());
        jTextFieldCoordenada.setText(coordenada + "");

        while(modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        
        String[] fila = new String[3];
        ArrayList<HashMap> lista = ctrlPI.getObrasAsociadasPI(coordenada);
        
        for(HashMap<String,String> obra: lista){
            fila[0] = obra.get("Identificador");
            fila[1] = obra.get("Titulo");
            fila[2] = obra.get("Autor");;
            modelo.addRow(fila);
        }
    }

