    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTableBusqueda.getModel();
        Admin_PI ctrlPI = Admin_PI.getInstance();
        
        while(modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        
        String[] fila = new String[3];
        ArrayList<HashMap> lista = ctrlPI.buscarObrasUbi(jComboBoxUbiObras.getSelectedItem().toString());
        
        
        if(lista.isEmpty()){
            jLabelError.setText("No hay obras en la ubicación seleccionada");
        } else {   
            for(HashMap<String,String> obra: lista){
                fila[0] = obra.get("Identificador");
                fila[1] = obra.get("Titulo");
                fila[2] = obra.get("Autor");
                modelo.addRow(fila);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTableRelacionadas.getModel();
        DefaultTableModel modelo1 = (DefaultTableModel) jTableBusqueda.getModel();
        
        boolean flag = true;
        
        String[] fila = new String[3];
        int seleccion = jTableBusqueda.getSelectedRow();
        
        if(seleccion != -1) {
            int i=0;
            do {
                if(i < jTableRelacionadas.getRowCount() && jTableBusqueda.getValueAt(seleccion, 0).toString().equals(jTableRelacionadas.getValueAt(i, 0).toString())) {
                    flag = false;
                    jLabelError.setText("La Obra ya está asociada");
                }
                i++;
            } while(i < jTableRelacionadas.getRowCount() && !jTableBusqueda.getValueAt(seleccion, 0).toString().equals(jTableRelacionadas.getValueAt(i, 0).toString()));
            
            if(flag) {
                fila[0] = jTableBusqueda.getValueAt(seleccion, 0).toString();
                fila[1] = jTableBusqueda.getValueAt(seleccion, 1).toString();
                fila[2] = jTableBusqueda.getValueAt(seleccion, 2).toString();

                modelo.addRow(fila);
                //modelo1.removeRow(seleccion);       
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

