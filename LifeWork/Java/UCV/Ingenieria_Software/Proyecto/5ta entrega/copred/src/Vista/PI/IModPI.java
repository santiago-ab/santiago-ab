    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed

        Admin_PI ctrlPI = Admin_PI.getInstance(); 
        String ubicacion = jComboBoxUbicacion.getSelectedItem().toString();
        int coordenada = Integer.parseInt(jTextFieldCoordenada.getText());
        ArrayList<String> secuencia_ID = new ArrayList();
        
        for(int fila = 0; fila < jTableRelacionadas.getRowCount(); fila++) {
            secuencia_ID.add(jTableRelacionadas.getValueAt(fila, 0).toString());    
        }
        
        String mensaje = ctrlPI.validarCoordenada(coordenada,coordenadaAnterior);
        
        if(mensaje.equals("")){
            IModDisp iDispoPI = new IModDisp(ubicacion, disponibilidad, coordenada, secuencia_ID, coordenadaAnterior);    
        
            iDispoPI.setVisible(true);
            iDispoPI.setLocationRelativeTo(null);
            this.dispose();
        } else {
            jLabelError.setText(mensaje);
        }    
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

